#include <unistd.h>

void	ft_print_comb(void)
{
	char	m[4];

	m[0] = '0';
	m[3] = ',';
	m[4] = ' ';
	while (m[0] <= '7')
	{
		m[1] = m[0] + 1;
		while (m[1] <= '8')
		{
			m[2] = m[1] + 1;
			while (m[2] <= '9')
			{
				if (m[0] != '7')
					write(1, m, 5);
				else
					write(1, m, 3);
				m[2]++;
			}
			m[1]++;
		}
		m[0]++;
	}
}
